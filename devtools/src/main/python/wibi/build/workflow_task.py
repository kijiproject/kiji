def name_from_artifact(artf):
    return "//%s:%s" % (artf.group_id.replace(".", "/"), artf.artifact_id)


def artifact_from_name(name):
    name = base.strip_prefix(name, "//")
    (group, aid) = name.split(":")
    group = group.replace("/", ".")
    return artifact.ArtifactName(group, aid)


